#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t size)
{
	size_t				i;
	unsigned char		*dest;
	const unsigned char	*source;

	i = 0;
	dest = (unsigned char *)dst;
	source = (const unsigned char *)src;
	while (i < size)
	{
		dest[i] = source[i];
		i++;
	}
	return (dest);
}
